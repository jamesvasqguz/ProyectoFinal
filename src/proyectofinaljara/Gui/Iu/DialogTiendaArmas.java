   private void actualizarListaArmas(List<arma> listaDeArmas) {
       this.observableListArmas.clear();
       this.observableListArmas.addAll(listaDeArmas);
   }
   private void actualizarListadoVehiculos(List<vehiculos> listaDeVehiculos) {
       this.observableListVehiculos.clear();
       this.observableListVehiculos.addAll(listaDeVehiculos);
   }

