    public void setVehiculoSeleccionado(vehiculos vehiculoSeleccionado) {
        vehiculos previous = this.vehiculoSeleccionado;
        if(vehiculoSeleccionado != null){
            this.vehiculoSeleccionado = vehiculoSeleccionado;
            txtVehiculoSeleccionado.setText(this.vehiculoSeleccionado.getNombreVehiculo());
        }
        firePropertyChange("Vehiculo Seleccionado", previous, this.vehiculoSeleccionado);
    }

    private void realizarCompra(arma armaAComprar, vehiculos vehiculo) {
        try {
             if (jugador.getOro() >= armaAComprar.getPrecio() ) {
                jugador.setOro(jugador.getOro() - armaAComprar.getPrecio());
                vehiculo.agregarArmas(armaAComprar);
                JOptionPane.showMessageDialog(this, "Felicidades se ralizo la compra.");
                txtOroDisponible.setText(Integer.toString(jugador.getOro()));
            } else {
                JOptionPane.showMessageDialog(this, "Lo siento no se ralizo la compra. \nOro Insuficiente");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lo siento no has seleccionado un vehiculo o arma.");
        }
           
    }
    
    public arma getArmaSeleccionada() {
        return armaSeleccionada;
    }

    public void setArmaSeleccionada(arma armaSeleccionada) {
        arma previous = this.armaSeleccionada;
        if(armaSeleccionada != null){
            try {
                this.armaSeleccionada = (arma) armaSeleccionada.clone();
                txtArmaSeleccionada.setText(this.armaSeleccionada.getNombreDelArma());
                txtTotal.setText(Integer.toString(armaSeleccionada.getPrecio()));
                botonComprar.setEnabled(true);
            } catch (CloneNotSupportedException ex) {
                Logger.getLogger(DialogCargarJuego.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        firePropertyChange("Arma Seleccionado", previous, this.vehiculoSeleccionado);
    }

